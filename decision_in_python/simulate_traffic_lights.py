colour = input("Enter any colour from Red , Yellow and Green = ").lower()

if colour == "red":
    print(" Colour is Red - You should STOP..!!")

elif colour == "yellow":
    print(" Colour is Yellow - Get Ready..!!")

elif colour == "green":
    print(" Colour is Green - Go!!")

else:
    print("Invalid colour for this Simulation..! Try again..!!")
